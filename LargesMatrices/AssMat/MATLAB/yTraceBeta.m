function yTraceBetareturn = yTraceBeta(x, y, beta, epsilon, nPeriod, eta)
  t1 = x - 0.1e1 / 0.2e1;
  t2 = t1 * beta;
  t3 = pi * y;
  t4 = 0.3e1 * t3;
  t5 = cos(t4);
  t9 = pi * x;
  t10 = sin(t9);
  t12 = 0.2e1 * t3;
  t13 = sin(t12);
  t20 = cos(t12);
  t21 = cos(t9);
  t23 = sin(t4);
  t26 = x - 0.1e1;
  t34 = x ^ 2;
  t36 = pi * (t34 - x);
  t37 = 0.2e1 / 0.3e1 * x;
  t39 = beta ^ 2;
  t42 = t5 ^ 2;
  t49 = t26 ^ 2;
  t53 = pi ^ 2;
  yTraceBetareturn = -0.6e1 / (0.9e1 * t42 * (t36 - t37 + 0.1e1 / 0.3e1) * t39 * (t36 + t37 - 0.1e1 / 0.3e1) - 0.4e1 * t5 * pi * t2 - 0.9e1 * t53 * (0.1e1 / 0.9e1 + t39 * t49 * t34)) * epsilon * pi * (-0.4e1 / 0.3e1 * t5 * t13 * t1 * t10 * beta + (-0.2e1 / 0.3e1 * t13 * t10 + t26 * x * beta * t23 * t21 * t20) * pi) * (t5 * t2 + pi / 0.2e1);
