function bGradSolBetareturn = bGradSolBeta(x, y, beta, epsilon, nPeriod)
  t1 = pi * x;
  t2 = sin(t1);
  t4 = beta * (x - 0.1e1 / 0.2e1);
  t5 = pi * y;
  t6 = 0.3e1 * t5;
  t7 = cos(t6);
  t12 = 0.2e1 * t5;
  t13 = sin(t12);
  t16 = cos(t12);
  t17 = cos(t1);
  t20 = sin(t6);
  t22 = x - 0.1e1;
  t28 = x ^ 2;
  t30 = pi * (t28 - x);
  t31 = 0.2e1 / 0.3e1 * x;
  t33 = beta ^ 2;
  t36 = t7 ^ 2;
  t43 = t22 ^ 2;
  t47 = pi ^ 2;
  t51 = sqrt(-0.9e1 * t36 * (t30 - t31 + 0.1e1 / 0.3e1) * t33 * (t30 + t31 - 0.1e1 / 0.3e1) + 0.4e1 * t7 * pi * t4 + 0.9e1 * t47 * (0.1e1 / 0.9e1 + t33 * t43 * t28));
  bGradSolBetareturn = 0.3e1 * epsilon / t51 * pi * (-0.4e1 / 0.3e1 * t13 * (t7 * t4 + pi / 0.2e1) * t2 + t22 * x * beta * t20 * pi * t17 * t16);
